// Tests: Hook integration — trailer generation with Copilot signals
// ------------------------------------------------------------------ //

describe("hook integration — Copilot trailer in commit-msg", () => {
  it("generates combined trailer with matches and Copilot signal", () => {
    // Simulate what the updated commitMsg() does:
    const matchCount = 2;
    const models = new Set(["gpt-4.1"]);
    const copilotSignals: CopilotSignal[] = [
      { source: "copilot-agent", confidence: "high", evidence: ["marker"] },
    ];

    const trailerParts: string[] = [];

    if (matchCount > 0) {
      trailerParts.push(`${matchCount} match(es)`);
      if (models.size > 0) {
        trailerParts.push(`model(s): ${[...models].join(", ")}`);
      }
    }

    if (copilotSignals.length > 0) {
      trailerParts.push(formatCopilotTrailer(copilotSignals));
    }

    const value = trailerParts.join("; ");
    assert.equal(
      value,
      "2 match(es); model(s): gpt-4.1; copilot (copilot-agent); confidence: high",
    );
  });

  it("generates Copilot-only trailer when no code patterns found", () => {
    const matchCount = 0;
    const copilotSignals: CopilotSignal[] = [
      { source: "copilot-cli", confidence: "high", evidence: ["GH_COPILOT"] },
    ];

    const trailerParts: string[] = [];

    if (matchCount > 0) {
      trailerParts.push(`${matchCount} match(es)`);
    }

    if (copilotSignals.length > 0) {
      trailerParts.push(formatCopilotTrailer(copilotSignals));
    }

    const value = trailerParts.join("; ");
    assert.equal(value, "copilot (copilot-cli); confidence: high");
  });
});
