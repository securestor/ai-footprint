describe("findGitRoot", () => {
  let tempRepo: string;

  beforeEach(() => {
    tempRepo = makeTempRepo();
    // Create a nested directory
    mkdirSync(join(tempRepo, "src", "deep"), { recursive: true });
  });

  afterEach(() => {
    cleanupDir(tempRepo);
  });

  it("finds root from the repo root itself", () => {
    const root = findGitRoot(tempRepo);
    assert.equal(root, tempRepo);
  });

  it("finds root from a nested directory", () => {
    const root = findGitRoot(join(tempRepo, "src", "deep"));
    assert.equal(root, tempRepo);
  });

  it("returns null when no .git directory exists", () => {
    const noGitDir = join(tmpdir(), `no-git-${Date.now()}`);
    mkdirSync(noGitDir, { recursive: true });
    try {
      const root = findGitRoot(noGitDir);
      // May find an ancestor .git — that's OK; just check it doesn't crash
      assert.ok(root === null || typeof root === "string");
    } finally {
      cleanupDir(noGitDir);
    }
  });
});

