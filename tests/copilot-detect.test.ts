import { describe, it, beforeEach, afterEach } from "node:test";
import assert from "node:assert/strict";
import {
  existsSync,
  mkdirSync,
  writeFileSync,
  rmSync,
} from "node:fs";
import { join } from "node:path";
import { tmpdir } from "node:os";
import {
  detectCopilotSignals,
  formatCopilotTrailer,
  writeCopilotMarker,
  clearCopilotMarker,
  findGitRoot,
} from "../core/copilot-detect.js";
import type { CopilotSignal } from "../core/copilot-detect.js";

// ------------------------------------------------------------------ //
// Helpers
// ------------------------------------------------------------------ //

function makeTempRepo(): string {
  const dir = join(tmpdir(), `ai-footprint-test-${Date.now()}-${Math.random().toString(36).slice(2)}`);
  mkdirSync(join(dir, ".git"), { recursive: true });
  return dir;
}

function cleanupDir(dir: string): void {
  try {
    rmSync(dir, { recursive: true, force: true });
  } catch { /* best-effort */ }
}

// ------------------------------------------------------------------ //
// Tests: detectCopilotSignals
// ------------------------------------------------------------------ //

describe("detectCopilotSignals", () => {
  let tempRepo: string;

  beforeEach(() => {
    tempRepo = makeTempRepo();
  });

  afterEach(() => {
    cleanupDir(tempRepo);
    // Clean env vars
    delete process.env.GH_COPILOT;
    delete process.env.GITHUB_COPILOT_CLI;
    delete process.env.GH_PROMPT;
  });

  it("returns empty array when no signals present", () => {
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.deepEqual(signals, []);
  });

  it("detects GH_COPILOT environment variable", () => {
    process.env.GH_COPILOT = "1";
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-cli");
    assert.equal(signals[0].confidence, "high");
    assert.ok(signals[0].evidence.some((e) => e.includes("GH_COPILOT")));
  });

  it("detects GITHUB_COPILOT_CLI environment variable", () => {
    process.env.GITHUB_COPILOT_CLI = "1";
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-cli");
  });

  it("detects GH_PROMPT with copilot mention", () => {
    process.env.GH_PROMPT = "copilot suggest";
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-cli");
    assert.ok(signals[0].evidence.some((e) => e.includes("GH_PROMPT")));
  });

  it("ignores GH_PROMPT without copilot mention", () => {
    process.env.GH_PROMPT = "something else";
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.equal(signals.length, 0);
  });

  it("detects 'Co-authored-by: copilot' in commit message", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "Fix bug\n\nCo-authored-by: Copilot <copilot@github.com>",
    });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-chat");
    assert.equal(signals[0].confidence, "medium");
  });

  it("detects 'Co-authored-by: [bot]' in commit message", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "Add feature\n\nCo-authored-by: github-actions[bot]",
    });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-chat");
  });

  it("detects 'github copilot' in commit message", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "This was generated by GitHub Copilot",
    });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-chat");
  });

  it("detects 'copilot agent' in commit message", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "refactor: copilot-agent applied cleanup",
    });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-chat");
  });

  it("detects 'Generated by Copilot' prefix", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "Generated by Copilot\n\nAdded helper function",
    });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-chat");
  });

  it("does not match unrelated commit messages", () => {
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "Fix authentication bug in login flow",
    });
    assert.equal(signals.length, 0);
  });

  it("detects marker file written by extension", () => {
    writeCopilotMarker(["src/app.ts", "src/utils.ts"], tempRepo);
    const signals = detectCopilotSignals({ repoRoot: tempRepo });
    assert.equal(signals.length, 1);
    assert.equal(signals[0].source, "copilot-agent");
    assert.equal(signals[0].confidence, "high");
    assert.deepEqual(signals[0].files, ["src/app.ts", "src/utils.ts"]);
  });

  it("combines multiple signals", () => {
    process.env.GH_COPILOT = "1";
    writeCopilotMarker(["index.ts"], tempRepo);
    const signals = detectCopilotSignals({
      repoRoot: tempRepo,
      commitMessage: "Co-authored-by: Copilot <copilot@github.com>",
    });
    assert.equal(signals.length, 3); // marker + CLI + commit msg
  });
});

