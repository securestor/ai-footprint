// Tests: formatCopilotTrailer
// ------------------------------------------------------------------ //

describe("formatCopilotTrailer", () => {
  it("returns empty string for no signals", () => {
    assert.equal(formatCopilotTrailer([]), "");
  });

  it("formats single CLI signal", () => {
    const signals: CopilotSignal[] = [
      { source: "copilot-cli", confidence: "high", evidence: ["GH_COPILOT"] },
    ];
    const trailer = formatCopilotTrailer(signals);
    assert.equal(trailer, "copilot (copilot-cli); confidence: high");
  });

  it("formats single agent signal", () => {
    const signals: CopilotSignal[] = [
      {
        source: "copilot-agent",
        confidence: "high",
        evidence: ["marker file"],
        files: ["a.ts"],
      },
    ];
    const trailer = formatCopilotTrailer(signals);
    assert.equal(trailer, "copilot (copilot-agent); confidence: high");
  });

  it("formats multiple sources with highest confidence", () => {
    const signals: CopilotSignal[] = [
      { source: "copilot-agent", confidence: "high", evidence: ["marker"] },
      { source: "copilot-chat", confidence: "medium", evidence: ["msg pattern"] },
    ];
    const trailer = formatCopilotTrailer(signals);
    assert.equal(
      trailer,
      "copilot (copilot-agent, copilot-chat); confidence: high",
    );
  });

  it("deduplicates sources", () => {
    const signals: CopilotSignal[] = [
      { source: "copilot-cli", confidence: "high", evidence: ["a"] },
      { source: "copilot-cli", confidence: "high", evidence: ["b"] },
    ];
    const trailer = formatCopilotTrailer(signals);
    assert.equal(trailer, "copilot (copilot-cli); confidence: high");
  });
});

