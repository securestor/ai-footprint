describe("copilot marker file management", () => {
  let tempRepo: string;

  beforeEach(() => {
    tempRepo = makeTempRepo();
  });

  afterEach(() => {
    cleanupDir(tempRepo);
  });

  it("creates marker directory and file", () => {
    writeCopilotMarker(["src/index.ts"], tempRepo);
    const markerPath = join(tempRepo, ".ai-footprint", "copilot-pending.json");
    assert.ok(existsSync(markerPath));

    const data = JSON.parse(
      require("node:fs").readFileSync(markerPath, "utf-8"),
    );
    assert.deepEqual(data.files, ["src/index.ts"]);
    assert.ok(data.updatedAt);
  });

  it("merges files across multiple writes", () => {
    writeCopilotMarker(["a.ts"], tempRepo);
    writeCopilotMarker(["b.ts"], tempRepo);

    const markerPath = join(tempRepo, ".ai-footprint", "copilot-pending.json");
    const data = JSON.parse(
      require("node:fs").readFileSync(markerPath, "utf-8"),
    );
    assert.deepEqual(data.files, ["a.ts", "b.ts"]);
  });

  it("deduplicates files", () => {
    writeCopilotMarker(["a.ts", "b.ts"], tempRepo);
    writeCopilotMarker(["a.ts", "c.ts"], tempRepo);

    const markerPath = join(tempRepo, ".ai-footprint", "copilot-pending.json");
    const data = JSON.parse(
      require("node:fs").readFileSync(markerPath, "utf-8"),
    );
    assert.deepEqual(data.files, ["a.ts", "b.ts", "c.ts"]);
  });

  it("clearCopilotMarker removes the file", () => {
    writeCopilotMarker(["a.ts"], tempRepo);
    const markerPath = join(tempRepo, ".ai-footprint", "copilot-pending.json");
    assert.ok(existsSync(markerPath));

    clearCopilotMarker(tempRepo);
    assert.ok(!existsSync(markerPath));
  });

  it("clearCopilotMarker is safe when file does not exist", () => {
    // Should not throw
    clearCopilotMarker(tempRepo);
  });
});

