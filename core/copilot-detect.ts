/**
 * AI agent detection module.
 *
 * Detects whether a commit was authored (wholly or partly) by an AI coding
 * agent. Supports multiple agents:
 *
 *  - **GitHub Copilot** (VS Code agent/chat, CLI `gh copilot`)
 *  - **Claude Code** (Anthropic CLI agent)
 *  - **Cursor** (AI-powered editor)
 *  - **Lovable** (AI app builder, formerly GPT Engineer)
 *
 * Detection signals (in order of reliability):
 *  1. Marker file written by the AI Footprint VS Code extension when it
 *     observes AI-agent-originated edits.
 *  2. Environment variables set by agent CLIs or editors.
 *  3. Commit message / author patterns commonly produced by each agent.
 */

import {
  existsSync,
  readFileSync,
  readdirSync,
  writeFileSync,
  unlinkSync,
  mkdirSync,
} from "node:fs";
import { join, dirname } from "node:path";
import { homedir } from "node:os";

// ------------------------------------------------------------------ //
// Types
// ------------------------------------------------------------------ //

/** Identifies which AI agent produced the code. */
export type AgentSource =
  | "copilot-agent"
  | "copilot-cli"
  | "copilot-chat"
  | "claude-code"
  | "cursor"
  | "lovable";

/** A single detected AI-agent signal. */
export interface AgentSignal {
  source: AgentSource;
  confidence: "high" | "medium" | "low";
  evidence: string[];
  /** Files identified as agent-edited (from marker file). */
  files?: string[];
  /** AI model name if known (e.g. "gpt-4.1", "claude-sonnet-4.5"). */
  model?: string;
}

// Keep backward-compat aliases
export type CopilotSignal = AgentSignal;

export interface AgentMarker {
  files: string[];
  /** Which agent wrote these files (optional — if absent, inferred). */
  agent?: string;
  /** AI model name reported by the agent (e.g. "gpt-4.1", "claude-sonnet-4.5"). */
  model?: string;
  updatedAt: string;
}

// Keep backward-compat alias
export type CopilotMarker = AgentMarker;

// ------------------------------------------------------------------ //
// Agent-specific detection definitions
// ------------------------------------------------------------------ //

interface EnvRule {
  envVar: string;
  /** If set, the env var value must contain this substring (case-insensitive). */
  valueContains?: string;
  label: string;
}

interface MsgRule {
  pattern: RegExp;
  label: string;
}

interface AgentDef {
  source: AgentSource;
  envConfidence: "high" | "medium" | "low";
  msgConfidence: "high" | "medium" | "low";
  envRules: EnvRule[];
  msgRules: MsgRule[];
  /** Check git author name/email for this pattern. */
  authorPatterns?: RegExp[];
}

