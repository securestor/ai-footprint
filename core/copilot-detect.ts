// Bot author patterns that indicate any AI agent
const GENERIC_BOT_PATTERN = /co-authored-by:\s*.*\[bot\]/i;

/** Marker file name (relative to repo root). */
const MARKER_DIR = ".ai-footprint";
const MARKER_FILE = "copilot-pending.json";
/** Persistent config — survives between commits (unlike the marker file). */
const CONFIG_FILE = "config.json";

interface AiFootprintConfig {
  /** Last known active Copilot model (written by VS Code extension). */
  copilotModel?: string;
  updatedAt?: string;
}

// ------------------------------------------------------------------ //
// Public API
// ------------------------------------------------------------------ //

/**
 * Detect AI-agent signals from multiple sources.
 * Returns an array of signals (may be empty).
 */
export function detectCopilotSignals(opts?: {
  commitMessage?: string;
  commitAuthor?: string;
  repoRoot?: string;
}): AgentSignal[] {
  const signals: AgentSignal[] = [];

  // 1. Marker file (highest reliability — written by our extension)
  const markerSignal = detectAgentMarker(opts?.repoRoot);
  if (markerSignal) signals.push(markerSignal);

  // 2. VS Code + Copilot environment detection (compound check)
  const vscCopilotSignal = detectVSCodeCopilot();
  if (vscCopilotSignal) signals.push(vscCopilotSignal);

  // 3. Per-agent environment variable checks
  for (const agent of AGENTS) {
    const envSignal = detectAgentEnv(agent);
    if (envSignal) signals.push(envSignal);
  }

  // 4. Commit message + author patterns
  if (opts?.commitMessage) {
    for (const agent of AGENTS) {
      const msgSignal = detectAgentMessage(agent, opts.commitMessage, opts.commitAuthor);
      if (msgSignal) signals.push(msgSignal);
    }

    // Generic [bot] author (if no specific agent matched it already)
    if (
      GENERIC_BOT_PATTERN.test(opts.commitMessage) &&
      !signals.some((s) => s.evidence.some((e) => e.includes("[bot]") || e.includes("Co-authored-by")))
    ) {
      signals.push({
        source: "copilot-chat",
        confidence: "low",
        evidence: ["Co-authored-by [bot] trailer (unknown agent)"],
      });
    }
  }

  return signals;
}

// Keep the old name as the canonical export — all callers already use it.
export { detectCopilotSignals as detectAgentSignals };

/**
 * Format agent signals into a trailer value string.
 */
export function formatCopilotTrailer(signals: AgentSignal[]): string {
  if (signals.length === 0) return "";

  const sources = [...new Set(signals.map((s) => s.source))];
  const highestConfidence = signals.some((s) => s.confidence === "high")
    ? "high"
    : signals.some((s) => s.confidence === "medium")
      ? "medium"
      : "low";

  // Group by tool family for cleaner output
  const families = [...new Set(sources.map(sourceFamily))];
  const label = families.length === 1
    ? `${families[0]} (${sources.join(", ")})`
    : sources.join(", ");

  // Include model name if any signal carries one
  const model = signals.find((s) => s.model)?.model;
  const modelPart = model ? `; model: ${model}` : "";

  return `${label}; confidence: ${highestConfidence}${modelPart}`;
}

export { formatCopilotTrailer as formatAgentTrailer };

/** Map a source to its human-readable family name. */
function sourceFamily(source: AgentSource): string {
  if (source.startsWith("copilot")) return "copilot";
  if (source === "claude-code") return "claude";
  if (source === "cursor") return "cursor";
  if (source === "lovable") return "lovable";
  return source;
}

// ------------------------------------------------------------------ //
// Marker file management (for the VS Code extension)
// ------------------------------------------------------------------ //

/**
 * Write or append to the agent marker file.
 * Called by the VS Code extension when AI-agent edits are detected.
 */
export function writeCopilotMarker(files: string[], repoRoot: string, agent?: string, model?: string): void {
  const dir = join(repoRoot, MARKER_DIR);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }

  const markerPath = join(dir, MARKER_FILE);

  // Merge with existing entries
  let existing: string[] = [];
  let existingAgent: string | undefined;
  let existingModel: string | undefined;
  if (existsSync(markerPath)) {
    try {
      const data = JSON.parse(readFileSync(markerPath, "utf-8")) as AgentMarker;
      if (Array.isArray(data.files)) existing = data.files;
      existingAgent = data.agent;
      existingModel = data.model;
    } catch {
      /* corrupted — overwrite */
    }
  }

  const merged = [...new Set([...existing, ...files])];
  const marker: AgentMarker = {
    files: merged,
    agent: agent ?? existingAgent,
    model: model ?? existingModel,
    updatedAt: new Date().toISOString(),
  };
  writeFileSync(markerPath, JSON.stringify(marker, null, 2));
}

export { writeCopilotMarker as writeAgentMarker };

// ------------------------------------------------------------------ //
// Persistent model config (survives between commits)
// ------------------------------------------------------------------ //

/**
 * Persist the active Copilot model to `.ai-footprint/config.json`.
 * Called by the VS Code extension whenever the model changes.
 * This file is NOT cleared after a commit — the git hook reads it.
 */
export function writeCopilotModelConfig(model: string, repoRoot: string): void {
  const dir = join(repoRoot, MARKER_DIR);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }
  const configPath = join(dir, CONFIG_FILE);
  let existing: AiFootprintConfig = {};
  if (existsSync(configPath)) {
    try {
      existing = JSON.parse(readFileSync(configPath, "utf-8")) as AiFootprintConfig;
    } catch { /* corrupted — overwrite */ }
  }
  writeFileSync(configPath, JSON.stringify({ ...existing, copilotModel: model, updatedAt: new Date().toISOString() }, null, 2));
}

/**
 * Read the persisted Copilot model from `.ai-footprint/config.json`.
 * Returns null if not found.
 */
export function readCopilotModelConfig(repoRoot?: string): string | null {
  const root = repoRoot ?? findGitRoot();
  if (!root) return null;
  const configPath = join(root, MARKER_DIR, CONFIG_FILE);
  if (!existsSync(configPath)) return null;
  try {
    const data = JSON.parse(readFileSync(configPath, "utf-8")) as AiFootprintConfig;
    return data.copilotModel ?? null;
  } catch {
    return null;
  }
}

/**
 * Clear the agent marker file after a commit.
 */
export function clearCopilotMarker(repoRoot: string): void {
  const markerPath = join(repoRoot, MARKER_DIR, MARKER_FILE);
  if (existsSync(markerPath)) {
    try {
      unlinkSync(markerPath);
    } catch {
      /* best-effort */
    }
  }
}

export { clearCopilotMarker as clearAgentMarker };

// ------------------------------------------------------------------ //
// Git root discovery (no shelling out)
// ------------------------------------------------------------------ //

/**
 * Walk upward from `startDir` to find the repo root (directory containing `.git`).
 */
export function findGitRoot(startDir?: string): string | null {
  let dir = startDir ?? process.cwd();
  for (;;) {
    if (existsSync(join(dir, ".git"))) return dir;
    const parent = dirname(dir);
    if (parent === dir) return null; // filesystem root reached
    dir = parent;
  }
}

// ------------------------------------------------------------------ //
// Internal detectors
// ------------------------------------------------------------------ //

function detectAgentMarker(repoRoot?: string): AgentSignal | null {
  const root = repoRoot ?? findGitRoot();
  if (!root) return null;

  const markerPath = join(root, MARKER_DIR, MARKER_FILE);
  if (!existsSync(markerPath)) return null;

  try {
    const data = JSON.parse(readFileSync(markerPath, "utf-8")) as AgentMarker;
    if (!Array.isArray(data.files) || data.files.length === 0) return null;

    const agentSource: AgentSource = (data.agent as AgentSource) ?? "copilot-agent";

    const evidenceLabel = `marker file lists ${data.files.length} AI-edited file(s)` +
      (data.agent ? ` (agent: ${data.agent})` : "") +
      (data.model ? ` (model: ${data.model})` : "");

    return {
      source: agentSource,
      confidence: "high",
      evidence: [evidenceLabel],
      files: data.files,
      model: data.model,
    };
  } catch {
    return null;
  }
}

/**
 * Detect VS Code + GitHub Copilot via environment variables.
 * VS Code sets VSCODE_GIT_ASKPASS_NODE, TERM_PROGRAM=vscode, etc.
 * Copilot presence is confirmed by PATH containing copilot-chat
 * or by the extension directory existing on disk.
 */
function detectVSCodeCopilot(): AgentSignal | null {
  const evidence: string[] = [];

  // Step 1: Confirm we're inside VS Code
  const inVSCode =
    process.env.TERM_PROGRAM === "vscode" ||
    !!process.env.VSCODE_GIT_ASKPASS_NODE ||
    !!process.env.VSCODE_GIT_IPC_HANDLE ||
    !!process.env.VSCODE_PID;

  if (!inVSCode) return null;
  evidence.push("running inside VS Code terminal");

  // Step 2: Confirm Copilot is present
  let copilotFound = false;

  // Check PATH for copilot-chat CLI injected by extension
  const pathVar = process.env.PATH ?? "";
  if (/github\.copilot/i.test(pathVar)) {
    copilotFound = true;
    evidence.push("PATH contains GitHub Copilot entry");
  }

  // Check extension directory on disk.
  // Also check .vscode-server/extensions for remote/SSH environments.
  if (!copilotFound) {
    const extDirs = [
      join(homedir(), ".vscode", "extensions"),
      join(homedir(), ".vscode-server", "extensions"),
    ];
    for (const extDir of extDirs) {
      if (copilotFound) break;
      try {
        if (existsSync(extDir)) {
          const entries = readdirSync(extDir);
          const copilotExt = entries.find((e) => /^github\.copilot/i.test(e));
          if (copilotExt) {
            copilotFound = true;
            evidence.push(`Copilot extension installed: ${copilotExt}`);
          }
        }
      } catch {
        /* best-effort */
      }
    }
  }

  if (!copilotFound) return null;

  // Step 3: Read persisted model from config file (written by VS Code extension)
  const model = readCopilotModelConfig() ?? undefined;
  if (model) {
    evidence.push(`model: ${model} (from .ai-footprint/config.json)`);
  }

  return {
    source: "copilot-agent",
    confidence: "high",
    evidence,
    model,
  };
}

function detectAgentEnv(agent: AgentDef): AgentSignal | null {
  const evidence: string[] = [];

  for (const rule of agent.envRules) {
    const val = process.env[rule.envVar];
    if (!val) continue;

    if (rule.valueContains) {
      if (val.toLowerCase().includes(rule.valueContains)) {
        evidence.push(rule.label);
      }
    } else {
      evidence.push(rule.label);
    }
  }

  if (evidence.length === 0) return null;
  return { source: agent.source, confidence: agent.envConfidence, evidence };
}

function detectAgentMessage(
  agent: AgentDef,
  message: string,
  author?: string,
): AgentSignal | null {
  const evidence: string[] = [];

  for (const rule of agent.msgRules) {
    if (rule.pattern.test(message)) {
      evidence.push(rule.label);
    }
  }

  // Check author patterns (e.g., lovable-dev[bot])
  if (author && agent.authorPatterns) {
    for (const ap of agent.authorPatterns) {
      if (ap.test(author)) {
        evidence.push(`git author matches ${ap.source}`);
      }
    }
  }

  if (evidence.length === 0) return null;
  return { source: agent.source, confidence: agent.msgConfidence, evidence };
}
